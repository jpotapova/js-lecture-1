// decisions: switch

var input = "S";

switch (input) {
  case "S":
    console.log("South");
    break;
  case "E":
    console.log("East");
    break;
  case "W":
    console.log("West");
    break;
  case "N":
    console.log("North");
    break;
  default:
    console.log("Incorrect input");
}
